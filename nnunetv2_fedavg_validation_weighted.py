def run_cmd(cmd: List[str], env: Optional[Dict[str, str]] = None, dry_run: bool = False) -> None:
    print("\n[CMD]", " ".join(cmd))
    if dry_run:
        print("[DRY RUN] skipped")
        return
    subprocess.run(cmd, env=env, check=True)


def newest_file(files: List[Path]) -> Path:
    files = [f for f in files if f.exists()]
    if not files:
        raise FileNotFoundError("No candidate files found")
    files.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    return files[0]


def safe_rm_tree(p: Path) -> None:
    if p.is_symlink() or p.is_file():
        p.unlink(missing_ok=True)
        return
    if p.is_dir():
        shutil.rmtree(p)


def find_dataset_folder(root: Path, dataset_id: int) -> str:
    pat = f"Dataset{dataset_id:03d}_*"
    matches = sorted([p.name for p in root.glob(pat) if p.is_dir()])
    if not matches:
        raise FileNotFoundError(f"Cannot find {pat} under {root}")
    if len(matches) > 1:
        print(f"[WARN] Multiple matches for dataset {dataset_id}: {matches}. Using the first one.")
    return matches[0]


def count_cases_in_imagesTr(raw_data_root: Path, dataset_folder: str) -> int:
    imagesTr = raw_data_root / dataset_folder / "imagesTr"
    if not imagesTr.exists():
        raise FileNotFoundError(f"imagesTr not found: {imagesTr}")

    rx = re.compile(r"^(?P<case>.+)_(?P<mod>\d{4})\.nii(\.gz)?$")
    cases = set()
    for f in imagesTr.iterdir():
        if f.is_file():
            m = rx.match(f.name)
            if m:
                cases.add(m.group("case"))

    if cases:
        return len(cases)

    # fallback (less reliable for multi-modality)
    all_nii = [x for x in imagesTr.iterdir()
               if x.is_file() and (x.name.endswith(".nii.gz") or x.name.endswith(".nii"))]
    if not all_nii:
        raise RuntimeError(f"No nii(.gz) files found in {imagesTr}")
    print("[WARN] Could not parse case ids by nnU-Net pattern; fallback to file count.")
    return len(all_nii)


def nnunet_cfg_dir(results_root: Path, dataset_folder: str, trainer: str, plans: str, config: str) -> Path:
    return results_root / dataset_folder / f"{trainer}__{plans}__{config}"


def nnunet_fold_dir(cfg_dir: Path, fold: int) -> Path:
    return cfg_dir / f"fold_{fold}"


def pick_checkpoint_from_tree(search_root: Path) -> Path:
    """
    Prefer final > best > latest
    """
    priority = ["checkpoint_final.pth", "checkpoint_best.pth", "checkpoint_latest.pth"]
    for name in priority:
        cands = list(search_root.rglob(name))
        if cands:
            return newest_file(cands)
    raise FileNotFoundError(f"No checkpoint*.pth found under {search_root}")


def ensure_checkpoint_final(folder: Path) -> None:
    """
    Ensure checkpoint_final.pth exists in folder. Create from best/latest if missing.
    folder: fold_0 directory OR all directory
    """
    final_p = folder / "checkpoint_final.pth"
    if final_p.exists():
        return
    for alt in ["checkpoint_best.pth", "checkpoint_latest.pth"]:
        alt_p = folder / alt
        if alt_p.exists():
            shutil.copy2(alt_p, final_p)
            print(f"[FIX] Created {final_p} from {alt_p}")
            return
    raise FileNotFoundError(f"No checkpoint_best/latest to create final in: {folder}")


def copy_cfg_and_fold(src_results_root: Path,
                      src_dataset_folder: str,
                      dst_results_root: Path,
                      trainer: str,
                      plans: str,
                      config: str,
                      fold: int,
                      overwrite: bool) -> Path:
    """
    Copy ONLY the needed cfg files + fold_0 directory from src_results_root to dst_results_root.
    Returns dst fold dir.
    """
    src_cfg = nnunet_cfg_dir(src_results_root, src_dataset_folder, trainer, plans, config)
    src_fold = nnunet_fold_dir(src_cfg, fold)
    if not src_cfg.exists() or not src_fold.exists():
        raise FileNotFoundError(f"Source cfg/fold missing:\n  {src_cfg}\n  {src_fold}")

    dst_cfg = nnunet_cfg_dir(dst_results_root, src_dataset_folder, trainer, plans, config)
    dst_fold = nnunet_fold_dir(dst_cfg, fold)

    if dst_cfg.exists():
        if overwrite:
            safe_rm_tree(dst_cfg.parent)
        else:
            raise FileExistsError(f"Destination exists: {dst_cfg.parent} (use --overwrite)")

    dst_cfg.mkdir(parents=True, exist_ok=True)

    # copy cfg-level files
    for item in src_cfg.iterdir():
        if item.is_file():
            shutil.copy2(item, dst_cfg / item.name)

    # copy fold dir
    shutil.copytree(src_fold, dst_fold)

    return dst_fold


def copy_model_tree(src_results_root: Path,
                    src_dataset_id: int,
                    raw_data_root: Path,
                    dst_results_root: Path,
                    dst_dataset_folder: str,
                    trainer: str,
                    plans: str,
                    config: str,
                    fold: int,
                    overwrite: bool) -> Path:
    """
    Copy a trained model tree from src_results_root/<DatasetXXX_name> to dst_results_root/dst_dataset_folder
    keeping nnU-Net structure. Returns destination fold directory path.
    """
    src_folder = find_dataset_folder(raw_data_root, src_dataset_id)
    src_cfg = nnunet_cfg_dir(src_results_root, src_folder, trainer, plans, config)
    src_fold = nnunet_fold_dir(src_cfg, fold)
    if not src_cfg.exists() or not src_fold.exists():
        raise FileNotFoundError(f"Source model tree not found:\n  {src_cfg}\n  {src_fold}")

    dst_cfg = nnunet_cfg_dir(dst_results_root, dst_dataset_folder, trainer, plans, config)
    dst_fold = nnunet_fold_dir(dst_cfg, fold)

    if dst_cfg.exists():
        if overwrite:
            safe_rm_tree(dst_cfg.parent)
        else:
            raise FileExistsError(f"Destination already exists: {dst_cfg.parent} (use --overwrite)")

    dst_cfg.mkdir(parents=True, exist_ok=True)

    # copy config-level files
    for item in src_cfg.iterdir():
        if item.is_file():
            shutil.copy2(item, dst_cfg / item.name)

    # copy fold dir
    shutil.copytree(src_fold, dst_fold)
    return dst_fold


def export_global_from_template(global_ckpt: Path,
                               template_dataset_folder: str,
                               results_root: Path,
                               new_dataset_folder: str,
                               trainer: str,
                               plans: str,
                               config: str,
                               fold: int,
                               overwrite: bool) -> Path:
    """
    Create a new Dataset<ID>_* in default results by copying the template model tree,
    then overwrite fold checkpoint_final.pth with global checkpoint.
    """
    template_cfg = nnunet_cfg_dir(results_root, template_dataset_folder, trainer, plans, config)
    template_fold = nnunet_fold_dir(template_cfg, fold)
    if not template_cfg.exists() or not template_fold.exists():
        raise FileNotFoundError(f"Template model tree missing:\n  {template_cfg}\n  {template_fold}")

    new_cfg = nnunet_cfg_dir(results_root, new_dataset_folder, trainer, plans, config)
    new_fold = nnunet_fold_dir(new_cfg, fold)

    if new_cfg.exists():
        if overwrite:
            safe_rm_tree(new_cfg.parent)
        else:
            raise FileExistsError(f"Destination exists: {new_cfg.parent} (use --overwrite)")

    new_cfg.mkdir(parents=True, exist_ok=True)

    # copy cfg-level files
    for item in template_cfg.iterdir():
        if item.is_file():
            shutil.copy2(item, new_cfg / item.name)

    shutil.copytree(template_fold, new_fold)

    # overwrite checkpoint_final
    shutil.copy2(global_ckpt, new_fold / "checkpoint_final.pth")
    # ensure final exists (already), but keep tidy if best/latest exist
    ensure_checkpoint_final(new_fold)
    return new_fold


