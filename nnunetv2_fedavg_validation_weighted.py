# Training (robust, with fallback copy)
# =========================
def train_client_once(dataset_id: int,
                      config: str,
                      fold: int,
                      trainer: str,
                      plans: str,
                      global_init: Path,
                      isolated_results_root: Path,
                      default_results_root: Path,
                      raw_data_root: Path,
                      env_base: Dict[str, str],
                      train_extra: List[str],
                      overwrite_isolated: bool,
                      dry_run: bool) -> Path:
    """
    Run nnUNetv2_train for a client, targeting isolated_results_root via env override.
    If outputs are not found under isolated_results_root, fallback to default_results_root
    and COPY produced fold tree into isolated_results_root.
    Returns checkpoint path inside isolated_results_root tree.
    """
    isolated_results_root.mkdir(parents=True, exist_ok=True)
    env = dict(env_base)

    # Set multiple vars to be safe (some setups still read legacy RESULTS_FOLDER)
    env["nnUNet_results"] = str(isolated_results_root)
    env["RESULTS_FOLDER"] = str(isolated_results_root)

    cmd = [
        "nnUNetv2_train", str(dataset_id), config, str(fold),
        "-tr", trainer,
        "-p", plans,
        "-pretrained_weights", str(global_init),
    ]
    cmd += train_extra

    log_path = isolated_results_root.parent / "train.log"
    print(f"[LOG] training output -> {log_path}")

    if dry_run:
        print("[DRY RUN] nnUNetv2_train skipped")
        return isolated_results_root / "DUMMY_CHECKPOINT.pth"

    # run training with stdout/stderr captured
    with open(log_path, "w") as f:
        subprocess.run(cmd, env=env, stdout=f, stderr=subprocess.STDOUT, check=True)

    # 1) try pick checkpoint in isolated
    try:
        ckpt = pick_checkpoint_from_tree(isolated_results_root)
        print(f"[INFO] picked checkpoint in isolated -> {ckpt}")
        return ckpt
    except FileNotFoundError:
        pass

    # 2) fallback: find in default results root then copy fold tree to isolated
    print("[WARN] No checkpoint found under isolated results. Fallback to default results root and copy into isolated...")

    src_dataset_folder = find_dataset_folder(raw_data_root, dataset_id)
    src_cfg = nnunet_cfg_dir(default_results_root, src_dataset_folder, trainer, plans, config)
    src_fold = nnunet_fold_dir(src_cfg, fold)
    if not src_fold.exists():
        raise FileNotFoundError(
            f"Fallback also failed: fold dir not found in default results.\n"
            f"Expected: {src_fold}\n"
            f"Check that trainer, plans, config, and fold match the training command."
        )

    # Copy cfg+fold into isolated results under same dataset folder name
    dst_dataset_folder = src_dataset_folder
    dst_cfg = nnunet_cfg_dir(isolated_results_root, dst_dataset_folder, trainer, plans, config)
    if dst_cfg.exists():
        if overwrite_isolated:
            safe_rm_tree(dst_cfg.parent)
        else:
            raise FileExistsError(f"Isolated already has {dst_cfg.parent}; use --overwrite to allow.")
    dst_cfg.mkdir(parents=True, exist_ok=True)

    # cfg-level files
    for item in src_cfg.iterdir():
        if item.is_file():
            shutil.copy2(item, dst_cfg / item.name)
    # fold dir
    shutil.copytree(src_fold, nnunet_fold_dir(dst_cfg, fold))

    # ensure final exists in the copied fold dir
    ensure_checkpoint_final(nnunet_fold_dir(dst_cfg, fold))

    ckpt = pick_checkpoint_from_tree(isolated_results_root)
    print(f"[INFO] picked checkpoint after fallback-copy -> {ckpt}")
    return ckpt


# =========================
# Optional initialization from an existing all-fold model
# =========================
def auto_find_dataset_all_ckpt(default_results_root: Path,
                               raw_data_root: Path,
                               dataset_id: int,
                               trainer: str,
                               plans: str,
                               config: str) -> Path:
    dataset_folder = find_dataset_folder(raw_data_root, dataset_id)
    all_dir = nnunet_cfg_dir(default_results_root, dataset_folder, trainer, plans, config) / "all"
    if not all_dir.exists():
        raise FileNotFoundError(f"All-fold model directory not found: {all_dir}")

    # prefer best then final then latest
    for name in ["checkpoint_best.pth", "checkpoint_final.pth", "checkpoint_latest.pth"]:
        p = all_dir / name
        if p.exists():
            return p
    raise FileNotFoundError(f"No checkpoint found in {all_dir}")


