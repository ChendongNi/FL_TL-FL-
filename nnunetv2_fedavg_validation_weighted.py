def extract_weights(ckpt: dict) -> Dict[str, torch.Tensor]:
    if "network_weights" in ckpt:
        return ckpt["network_weights"]
    if "state_dict" in ckpt:
        return ckpt["state_dict"]
    raise KeyError(f"Cannot find weights key in checkpoint. Keys={list(ckpt.keys())}")


def fedavg_state_dict(state_dicts: List[Dict[str, torch.Tensor]], weights: List[float]) -> Dict[str, torch.Tensor]:
    w = torch.tensor(weights, dtype=torch.float64)
    w = torch.clamp(w, min=1e-6)
    w = w / w.sum()

    keys0 = list(state_dicts[0].keys())
    for i, sd in enumerate(state_dicts[1:], start=1):
        if list(sd.keys()) != keys0:
            raise RuntimeError(f"State dict keys mismatch between client0 and client{i}")

    out: Dict[str, torch.Tensor] = {}
    for k in keys0:
        vals = [sd[k] for sd in state_dicts]
        v0 = vals[0]
        if torch.is_floating_point(v0):
            stacked = torch.stack([v.to(torch.float64) for v in vals], dim=0)
            flat = stacked.view(len(vals), -1)
            avg_flat = (w[:, None] * flat).sum(dim=0)
            out[k] = avg_flat.view_as(v0).to(v0.dtype)
        else:
            out[k] = v0
    return out


def make_global_ckpt(base_ckpt: dict, avg_weights: Dict[str, torch.Tensor]) -> dict:
    out = dict(base_ckpt)
    if "network_weights" in out:
        out["network_weights"] = avg_weights
    elif "state_dict" in out:
        out["state_dict"] = avg_weights
    else:
        out["network_weights"] = avg_weights
    for drop in ["optimizer_state", "grad_scaler_state"]:
        if drop in out:
            out[drop] = None
    return out


# =========================
# Generic export ID helpers
# =========================
def id_global(round_idx: int, global_id_offset: int) -> int:
    """Generate a generic nnU-Net dataset ID for an exported global model."""
    return global_id_offset + round_idx

def id_local(dataset_id: int, round_idx: int, local_id_multiplier: int) -> int:
    """Generate a generic nnU-Net dataset ID for an exported local client model."""
    return dataset_id * local_id_multiplier + round_idx

def folder_global(round_idx: int, global_id_offset: int) -> str:
    model_id = id_global(round_idx, global_id_offset)
    return f"Dataset{model_id:03d}_global_round{round_idx:03d}"

def folder_local(dataset_id: int, round_idx: int, fold: int, local_id_multiplier: int) -> str:
    model_id = id_local(dataset_id, round_idx, local_id_multiplier)
    return f"Dataset{model_id:03d}_client{dataset_id}_local_round{round_idx:03d}_fold{fold}"


