def load_splits_val_cases(nnUNet_preprocessed_root: Path, dataset_id: int, fold: int) -> List[str]:
    pre_folder = find_dataset_folder(nnUNet_preprocessed_root, dataset_id)
    splits_file = nnUNet_preprocessed_root / pre_folder / "splits_final.json"
    if not splits_file.exists():
        raise FileNotFoundError(f"splits_final.json not found: {splits_file}")
    splits = json.loads(splits_file.read_text())
    if not isinstance(splits, list) or fold >= len(splits):
        raise RuntimeError(f"Invalid splits_final.json or fold out of range: fold={fold}, file={splits_file}")
    fold_split = splits[fold]
    val_cases = fold_split.get("val", [])
    if not isinstance(val_cases, list) or not val_cases:
        raise RuntimeError(f"Empty/invalid val split for fold {fold}: {splits_file}")
    return val_cases


def build_val_symlink_folders(raw_data_root: Path,
                              dataset_id: int,
                              val_cases: List[str],
                              out_root: Path,
                              overwrite: bool) -> Tuple[Path, Path]:
    dataset_folder = find_dataset_folder(raw_data_root, dataset_id)
    imagesTr = raw_data_root / dataset_folder / "imagesTr"
    labelsTr = raw_data_root / dataset_folder / "labelsTr"
    if not imagesTr.exists() or not labelsTr.exists():
        raise FileNotFoundError(f"imagesTr/labelsTr missing under {raw_data_root}/{dataset_folder}")

    img_out = out_root / "imagesTs"
    lab_out = out_root / "labelsTs"

    if out_root.exists() and overwrite:
        safe_rm_tree(out_root)

    img_out.mkdir(parents=True, exist_ok=True)
    lab_out.mkdir(parents=True, exist_ok=True)

    for case in val_cases:
        # images
        candidates = list(imagesTr.glob(f"{case}_*.nii.gz")) + list(imagesTr.glob(f"{case}_*.nii"))
        if not candidates:
            raise FileNotFoundError(f"No image files for case '{case}' in {imagesTr}")
        for f in candidates:
            dst = img_out / f.name
            if not dst.exists():
                dst.symlink_to(f)

        # label
        lab_candidates = list(labelsTr.glob(f"{case}.nii.gz")) + list(labelsTr.glob(f"{case}.nii"))
        if not lab_candidates:
            raise FileNotFoundError(f"No label file for case '{case}' in {labelsTr}")
        lf = lab_candidates[0]
        dstl = lab_out / lf.name
        if not dstl.exists():
            dstl.symlink_to(lf)

    return img_out, lab_out


def find_plans_file(nnUNet_preprocessed_root: Path, dataset_id: int, plans_name: str) -> Path:
    pre_folder = find_dataset_folder(nnUNet_preprocessed_root, dataset_id)
    base = nnUNet_preprocessed_root / pre_folder
    candidates = [
        base / f"{plans_name}.json",
        base / "nnUNetPlans.json",
        base / "plans.json",
    ]
    candidates = [c for c in candidates if c.exists()]
    if candidates:
        return candidates[0]
    found = [p for p in base.glob("*.json") if p.is_file()]
    if not found:
        raise FileNotFoundError(f"No plans json found in {base}")
    return newest_file(found)


def extract_val_dice_from_summary(summary_json: Path) -> float:
    data = json.loads(summary_json.read_text())

    if isinstance(data, dict):
        fg = data.get("foreground_mean")
        if isinstance(fg, dict) and isinstance(fg.get("Dice"), (int, float)):
            return float(fg["Dice"])

        mean = data.get("mean")
        if isinstance(mean, dict):
            if isinstance(mean.get("Dice"), (int, float)):
                return float(mean["Dice"])
            dices = []
            for _, v in mean.items():
                if isinstance(v, dict) and isinstance(v.get("Dice"), (int, float)):
                    dices.append(float(v["Dice"]))
            if dices:
                return sum(dices) / len(dices)

    raise RuntimeError(f"Cannot parse Dice from summary: {summary_json}")


def predict_and_eval_val(default_results_root: Path,
                         model_dataset_id: int,
                         raw_data_root: Path,
                         nnUNet_preprocessed_root: Path,
                         dataset_id_for_val: int,
                         config: str,
                         trainer: str,
                         plans: str,
                         fold: int,
                         work_dir: Path,
                         np_eval: int,
                         overwrite: bool,
                         dry_run: bool) -> Tuple[Path, float]:
    val_cases = load_splits_val_cases(nnUNet_preprocessed_root, dataset_id_for_val, fold)
    img_dir, lab_dir = build_val_symlink_folders(raw_data_root, dataset_id_for_val, val_cases, work_dir / "valset", overwrite)

    pred_dir = work_dir / "pred"
    pred_dir.mkdir(parents=True, exist_ok=True)

    cmd_pred = [
        "nnUNetv2_predict",
        "-d", str(model_dataset_id),
        "-c", config,
        "-tr", trainer,
        "-p", plans,
        "-f", str(fold),
        "-chk", "checkpoint_final.pth",
        "-i", str(img_dir),
        "-o", str(pred_dir),
        "--disable_tta"
    ]
    run_cmd(cmd_pred, env=None, dry_run=dry_run)

    dataset_folder = find_dataset_folder(raw_data_root, dataset_id_for_val)
    djfile = raw_data_root / dataset_folder / "dataset.json"
    pfile = find_plans_file(nnUNet_preprocessed_root, dataset_id_for_val, plans)

    summary_json = work_dir / "summary.json"
    cmd_eval = [
        "nnUNetv2_evaluate_folder",
        "-djfile", str(djfile),
        "-pfile", str(pfile),
        "-o", str(summary_json),
        "-np", str(np_eval),
        str(lab_dir),
        str(pred_dir),
    ]
    run_cmd(cmd_eval, env=None, dry_run=dry_run)

    if dry_run:
        return summary_json, 0.0

    dice = extract_val_dice_from_summary(summary_json)
    return summary_json, dice


