#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Generic single-GPU Federated Averaging (FedAvg) for nnU-Net v2 with
validation-metric-weighted aggregation.

This public/shareable script trains multiple nnU-Net v2 client datasets
sequentially on one GPU. After each local training step, it evaluates the local
model on that client's validation split using official nnU-Net v2 prediction and
evaluation commands. The client validation Dice scores can then be used as
aggregation weights for subsequent FedAvg rounds.

Workflow per round:
  1. Start from the previous global checkpoint.
  2. Train each client dataset sequentially with the same global initialization.
  3. Export each local model into an nnU-Net-compatible results tree.
  4. Predict each client's validation set and compute validation Dice.
  5. Aggregate local checkpoints with FedAvg:
       - round 1 may use case-count weights or Dice weights;
       - later rounds use validation-Dice weights by default.
  6. Export the resulting global checkpoint for inference with nnUNetv2_predict.

Before running, prepare:
  1. nnU-Net v2 raw, preprocessed, and results directories.
  2. Client datasets in nnU-Net v2 format.
  3. Valid ``splits_final.json`` files for all client datasets.
  4. A shared model setup across all clients:
       same configuration, trainer, plans, fold, modalities, and label schema.
  5. Either:
       --init_global <PATH_TO_INITIAL_CHECKPOINT.pth>
     or:
       --init_from_dataset_all <DATASET_ID_WITH_PRETRAINED_ALL_FOLD_MODEL>

Example command with placeholders:
  python nnunetv2_fedavg_validation_weighted.py \
    --fl_root <PATH_TO_FL_OUTPUT_DIRECTORY> \
    --raw_data_root <PATH_TO_NNUNET_RAW> \
    --results_root <PATH_TO_NNUNET_RESULTS> \
    --nnUNet_preprocessed <PATH_TO_NNUNET_PREPROCESSED> \
    --datasets <CLIENT_DATASET_ID_1> <CLIENT_DATASET_ID_2> \
    --rounds <NUMBER_OF_FL_ROUNDS> \
    --config <NNUNET_CONFIG> \
    --trainer <TRAINER_NAME> \
    --plans <PLANS_NAME> \
    --fold <FOLD_ID> \
    --init_global <PATH_TO_INITIAL_CHECKPOINT.pth>

Notes:
  - This script intentionally contains no personal paths, usernames, or
    institution-specific identifiers.
  - Dataset export IDs are generated by configurable generic formulas:
       global_id = --global_id_offset + round
       local_id  = dataset_id * --local_id_multiplier + round
  - Use ``--dry_run`` first to inspect commands before launching training.
"""

